#!/usr/bin/env bash
# Строгий режим shell: ошибка/неинициализированная переменная/ошибка в pipe останавливают выполнение.
set -euo pipefail

# Определяем корень репозитория относительно текущего файла.
REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
# Переходим в корневую папку проекта.
cd "$REPO_ROOT"

# Запускаем GNN-скрипт, прокидывая все аргументы пользователя.
python -m src.train_gnn "$@"
